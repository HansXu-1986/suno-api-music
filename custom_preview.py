#!/usr/bin/env python3
"""
Custom generation preview helper
Generate lyrics preview with metadata tags before sending to Suno
"""

from typing import Dict, List

def format_preview(answers: Dict) -> str:
    """Format complete preview with metadata and lyrics"""
    
    prompt = answers.get("prompt", "")
    style = answers.get("style", "")
    title = answers.get("title", "")
    lyrics = answers.get("lyrics", "")
    instrumental = answers.get("instrumental", False)
    versions = answers.get("versions", 2)
    
    # If lyrics is empty, generate based on prompt
    if not lyrics:
        lyrics = generate_lyrics_preview(prompt, style)
    
    # Add metadata tags
    preview_lines = []
    
    if title:
        preview_lines.append(f"[title: {title}]")
    if style:
        preview_lines.append(f"[genre: {style}]")
    
    # Guess instruments based on style
    instruments = guess_instruments(style)
    if instruments:
        preview_lines.append(f"[instrument: {instruments}]")
    
    preview_lines.append(f"[style: {emoji_style(style)}]")
    preview_lines.append("")
    preview_lines.append(lyrics)
    
    return "\n".join(preview_lines)


