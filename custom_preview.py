def guess_instruments(style: str) -> str:
    """Guess instruments based on style"""
    style_lower = style.lower()
    
    if "民谣" in style_lower or "folk" in style_lower:
        return "木吉他, 口琴, 轻鼓点"
    elif "摇滚" in style_lower or "rock" in style_lower:
        return "电吉他, 贝斯, 架子鼓"
    elif "流行" in style_lower or "pop" in style_lower:
        return "钢琴, 吉他, 鼓点"
    elif "rap" in style_lower or "说唱" in style_lower:
        return "beat, 贝斯"
    elif "爵士" in style_lower or "jazz" in style_lower:
        return "萨克斯, 爵士鼓, 贝斯, 钢琴"
    elif "古典" in style_lower:
        return "钢琴"
    elif "电子" in style_lower:
        return "合成器, 鼓机"
    
    return "吉他, 鼓"


def emoji_style(style: str) -> str:
    """Convert style to descriptive text"""
    if "民谣" in style:
        return "娓娓道来, 抒情走心"
    if "摇滚" in style:
        return "激情奔放,  energetic"
    if "流行" in style:
        return "流畅悦耳, 朗朗上口"
    if "说唱" in style or "rap" in style:
        return "节奏强劲, 歌词犀利"
    
    return style


def summarize_all(answers: Dict) -> str:
    """Generate full summary for user confirmation"""
    prompt = answers.get("prompt", "")
    style = answers.get("style", "")
    title = answers.get("title", "自动生成")
    instrumental = "是" if answers.get("instrumental", False) else "否"
    versions = answers.get("versions", 2)
    
    preview = format_preview(answers)
    
    summary = [
        "📋 **生成信息确认预览**\n",
        f"**标题**: {title}",
        f"**主题/歌词大意**: {prompt}",
        f"**曲风**: {style}",
        f"**纯音乐**: {instrumental}",
        f"**生成版本数**: {versions}",
        "",
        "🎵 **歌词预览（带元标签）**:",
        "```",
        preview,
        "```",
        "",
        "✓ 确认以上信息无误，请回复「确认」开始生成",
        "✓ 如果需要修改，请告诉我需要修改哪一项",
    ]
    
    return "\n".join(summary)
